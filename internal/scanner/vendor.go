// LookupVendor returns the vendor name for a MAC address.
func LookupVendor(mac string) string {
	mac = strings.ToUpper(strings.ReplaceAll(mac, "-", ":"))

	// Try first 3 octets (most common)
	if len(mac) >= 8 {
		prefix := mac[:8]
		if vendor, ok := ouiDB[prefix]; ok {
			return vendor
		}
	}

	// Check if locally administered (bit 1 of first byte set)
	if len(mac) >= 2 {
		firstByte := mac[0:2]
		var val uint8
		fmt.Sscanf(firstByte, "%X", &val)
		if val&0x02 != 0 {
			return "Private (Locally Administered)"
		}
	}

	return "Unknown"
}
